    private void cajaPasswordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cajaPasswordFocusGained
        // TODO add your handling code here:
         if (cajaPassword.getText().equals("Ingrese Contraseña")) {
            cajaPassword.setEchoChar('●');
            cajaPassword.setText("");
            cajaPassword.setForeground(Color.black);
            cajaPassword.setFont(new Font("Segoe UI", Font.PLAIN, 14));
        }
        
    }//GEN-LAST:event_cajaPasswordFocusGained

    private void cajaPasswordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cajaPasswordFocusLost
        // TODO add your handling code here:
        if (cajaPassword.getText().isEmpty()) {
            cajaPassword.setEchoChar((char) 0);
            cajaPassword.setText("Ingrese Contraseña");
            cajaPassword.setForeground(Color.gray);
            cajaPassword.setFont(new Font("Segoe UI", Font.PLAIN, 10));
        }
    }//GEN-LAST:event_cajaPasswordFocusLost

    private void cajaUserFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cajaUserFocusLost
        // TODO add your handling code here:
       if (cajaUser.getText().isEmpty()) {
            cajaUser.setText("Ingrese Usuario");
            cajaUser.setForeground(Color.gray);
            cajaUser.setFont(new Font("Sitka Text", Font.PLAIN, 10));
        }
    }//GEN-LAST:event_cajaUserFocusLost

