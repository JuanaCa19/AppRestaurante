    private void icVerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_icVerMouseClicked
        // TODO add your handling code here:
        if(this.x==0){
            cajaPassword.setEchoChar((char) 0);
            x=1;
            ImageIcon icono2 = new ImageIcon(getClass().getResource("/picture/ocultar.png"));
            Image imagen2 = icono2.getImage().getScaledInstance(
                icVer.getWidth(),
                icVer.getHeight(),
                Image.SCALE_SMOOTH
            );
            icVer.setIcon(new ImageIcon(imagen2));
        }else{
            ImageIcon icono2 = new ImageIcon(getClass().getResource("/picture/ver.png"));
            Image imagen2 = icono2.getImage().getScaledInstance(
                icVer.getWidth(),
                icVer.getHeight(),
                Image.SCALE_SMOOTH
            );
            icVer.setIcon(new ImageIcon(imagen2));
            cajaPassword.setEchoChar('●');
            this.x=0;
        }
    }//GEN-LAST:event_icVerMouseClicked

